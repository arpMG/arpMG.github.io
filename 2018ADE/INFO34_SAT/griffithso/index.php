                  <h2 class="text-uppercase">What Other Studies Found</h2>
                  <p class="item-intro text-muted"> </p>
                  <iframe width="560" height="315" src="https://www.youtube.com/embed/Czg_9C7gw0o" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe>
                  <p>TEDx Talks. (2017). Is Social Media Hurting Your Mental Health? | Bailey Parnell | TEDxRyersonU. [Online Video]. 22 June 2017. Available from: https://www.youtube.com/watch?v=Czg_9C7gw0o. [Accessed: 13 August 2018].</p>
                  <br>
                  <br>
                  <p>From the research conducted by others in similar fields, it is shown that social media does seem to have an effect on mental health. Although, the amount of time spent on social media may not be the best indicator as to what symptoms people may experience. Other studies suggested that the best way to predict depressive symptoms was to look at status updates and the posts people made on their social platform. </p>
                  <p>In a study published by National Center for Biotechnology Information, it was actually found that social media could also have positive effects and rewards for mental health. For example, they found that going over previously posted photos produced pleasant memories enhancing the person’s wellbeing and that the friends a individual has online strongly correlates with being a friend in real life. </p>
